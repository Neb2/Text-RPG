def player_health_bar(character):
    hp_bar = ""
    hp_ticks = (character.base_hp / character.hp_max) * 100 / 4

    while hp_ticks > 0:
        hp_bar += Colours.GREEN + "█" + Colours.END
        hp_ticks -= 1

    print(hp_bar)


def player_attack_bar(character):
    hp_bar = ""
    hp_ticks = (character.b_atk_p / character.m_atk_p) * 100 / 4

    while hp_ticks > 0:
        hp_bar += Colours.RED1 + "█" + Colours.END
        hp_ticks -= 1

    print(hp_bar)


def enemy_health_bar(en1):
    hp_bar = ""
    hp_ticks = (en1.hp / en1.max_hp) * 100 / 4

    while hp_ticks > 0:
        hp_bar += Colours.RED + "█" + Colours.END
        hp_ticks -= 1

    print(hp_bar)


